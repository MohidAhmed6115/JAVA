public class maths {
    public static void main(String[] args) {
        // value of pi
        System.out.println("Value of PI            : " + Math.PI);
        // value of eulers number
        System.out.println("Value of E             : " + Math.E);
        // calculating power
        System.out.println("Power of 2 raised to 3 : " + Math.pow(2, 3));
        // absolute value
        System.out.println("Absolute Value         : " + Math.abs(-53));
        // squareroot value
        System.out.println("Square Root of 5184    : " + Math.sqrt(5184));
        // round off value
        System.out.println("Roundoff value for 8.5 : " + Math.round(8.5));
        // ceiling value
        System.out.println("Ceiled value for 3.2   : " + Math.ceil(3.2));
        // flooring value
        System.out.println("Floored value for 2.9  : " + Math.floor(2.9));
        // Max value
        System.out.println("Max value between 4 & 5: " + Math.max(4, 5));
        // Min value
        System.out.println("Min value between 4 & 5: " + Math.min(4, 5));
    }
}